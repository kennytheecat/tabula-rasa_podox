<h1>Help</h1>
<h2>Add Weekly Bulletins</h2>
<ol>
	<li>Click "Posts" on left sidebar</li>
	<li>Click "Add New"</li>
	<li>Where it says, "Enter title here" type the date of the bulletin</li>
	<li>Click the "Add Media" button</li>
	<li>From this screen you can drag and drop the file or find it after clicking the "Upload Files" button</li>
	<li>When you do so, it loads. Do not click "Insert into post", just click the x at the top right.</li>
	<li>On the right hand sidebar, in the "Categories" section, check the box that "Weekly Bulletins"</li>
	<li>On the right hand sidebar, in the "Featured Image" section, click "Set featured image"</li>
	<li>On this screen, you can drag and drop the image like was done for the pdf file</li>
		<ol style="padding-left: 20px;">
			<li>If you do not have an iamge, this is what i typically do</li>
			<li>Open the pdf</li>
			<li>Reduce the size so it fits your browser window.</li>
			<li>Click print screen on your computer, to take an image of your screen</li>
			<li>Take that image and upload it into your image editor of choice, and crop the image so it only displays the image of the bulletin.</li>
			<li>Save it and then upload it to the post</li>
		</ol>
	<li>Click "Set featured image" after image uploads</li>
	<li>On the right hand sidebar, in the "Publish" section at the top, click "Publish"</li>
	<li>Done.</li>
</ol>

<h2>Add Photo Gallery</h2>
<ol>
	<li>Click "Posts" on left sidebar</li>
	<li>Click "Add New"</li>
	<li>Where it says, "Enter title here" type a title for the gallery</li>
	<li>Click the "Add Media" button</li>
	<li>From this screen you can drag and drop the photos or find them after clicking the "Upload Files" button</li>
	<li>When you do so, it loads. Do not click "Insert into post", just click the x at the top right.</li>
	<li>On the right hand sidebar, in the "Categories" section, check the box that "Gallery"</li>
	<li>On the right hand sidebar, in the "Featured Image" section, click "Set featured image"</li>
	<li>On this screen, choose the image you want to be the featured image( the one that shows up throughout the sits on listing sections)</li>
	<li>Click "Set featured image" after image uploads</li>
	<li>On the right hand sidebar, in the "Publish" section at the top, click "Publish"</li>
	<li>Done.</li>
</ol>